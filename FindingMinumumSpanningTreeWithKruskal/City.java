public class City {
    
private int x,y;
private String name;




public City(int x ,int y ,String name){
this.x=x;
this.y=y;
this.name=name;
 }

public void setY(int y) {
    this.y = y;
}

public int getX() {
    return x;
}

public int getY() {
    return y;
}

 public String getName() {
    return name;
}

public void setName(String name) {
    this.name = name;
}


}
